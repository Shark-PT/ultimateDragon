        <p>
            <b>Historia</b>
            <p>
                A academia encontra-se aberta desde o dia 8 de Janeiro de 2013, como DAMTM (delegação de Artes Marciais Tiago Mendes).
                Inicialmente a mesma apenas estava direcionada para a pratica de King Fu Tradicional e Sanda.
            </p>
            <p>
                Competindo nas duas modalidades acima mencionadas, em 10 competições, conseguimos conquistar 10 titulos de campeões mundiais e 17 nacionais.
            </p>
            <p>
                A partir de 2014, a academia começou a "viver" novas experiências devido a iniciação da pratica de outras artes marciais.
            </p>
            <p>
                Em 2017 foi criada a UltimateDragon, com o objectivo de ser uma casa aberta a todas as artes marciais, desportos de combate e sistema de defesa pessoal.
            </p>
            
        </p>
        <p>
            <b>Regras</b>
            <ol>
                <li>Apenas usar o que aprende em legitima defesa e para fins pacificos. O uso fora da escola para ataque, exibição ou intimidação é expressamente proibido</li>
                <li>Respeitar a arte marcial e toda a sua envolvencia</li>
                <li>Respeitar o mestre</li>
                <li>Respeitar os colegas de treino</li>
                <li>Respeitar o espaço e o material de treino</li>
                <li>Obedecer a todas as ordens dos ateltas mais graduados ou mais antigos</li>
                <li>Treinar com empenho e concentração</li>
                <li>Estar atento durante a demonstração e/ou exposição de algum exercicio</li>
                <li>Sempre que tiver alguma duvida, perguntar as vezes que forem necessárias até entender o que relamente é pedido</li>
                <li>Sempre que quiser falar de alguma coisa, que não seja sobre os treinos,m é necessário levantar a mão no ar e aguardar qu o mestre dê permissão para falar.</li>
                <li>Não trazer os "problemas de fora" para o trino.</li>
                <li>Pedir sempre que quiser beber, comer, ir à casa de banho ou fazer pausa no treino.</li>
                <li>Retirar qualquer tipo de adereço(brincos, pulseiras, colares, rellógios...)</li>
                <li>Não se esquecer do material.</li>
                <li>Não se esquecer do treino.</li>
                <li>Não chegar atrasado.</li>
                <li>Não ser preguiçoso.</li>
                <li>Não ser resmungão.</li>
                <li>Em nenhuma situação podem ficar "amuados".</li>
                <li>Informar sempre o mestre de tudo o que diz respeito à escola.</li>
                <li>Proibido o uso de telemóvel sem permissão.</li>
                <li>O treino é realizado descalço ou com calçado apropriado, desde que devidamente aceite pelo mestre</li>

            </ol>
        </p>
